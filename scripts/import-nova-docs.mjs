import { cp, mkdir, readFile, readdir, rm, writeFile } from 'node:fs/promises'
import path from 'node:path'
import vm from 'node:vm'
import { fileURLToPath } from 'node:url'

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url))
const repositoryRoot = path.resolve(scriptDirectory, '..')
const sourceApplication = path.resolve(
  process.env.NOVA_DOCS_SOURCE ?? path.join(repositoryRoot, '..', 'egorkozelskij-nova-docs'),
)
const sourceDocumentsRoot = path.join(sourceApplication, 'src/features/docs/documents/public')
const sourceNavigationPath = path.join(sourceApplication, 'src/features/docs/config/docs-navigation.ts')
const targetDocumentsRoot = path.join(repositoryRoot, 'docs/nova')
const targetNavigationPath = path.join(repositoryRoot, 'docs/.vitepress/nova-sidebar.generated.ts')

const documentationTitles = {
  core: 'NovaCore',
  uikit: 'NovaUIKit',
  advanced: 'NovaAdvanced',
  'timeline-chart': 'Timeline chart',
}

assertSafeTarget(targetDocumentsRoot)

const navigationSource = await readFile(sourceNavigationPath, 'utf8')
const docsTree = readDocsTree(navigationSource)

await rm(targetDocumentsRoot, { recursive: true, force: true })
await mkdir(targetDocumentsRoot, { recursive: true })

const actualDocuments = await copyPublicDocuments()
const sidebar = createSidebar(docsTree, actualDocuments)

await writeFile(
  targetNavigationPath,
  `// Generated by scripts/import-nova-docs.mjs. Do not edit manually.\n\nexport const novaSidebar = ${JSON.stringify(sidebar, null, 2)}\n\nexport const novaDocumentCount = ${actualDocuments.length}\n`,
  'utf8',
)

console.log(`Imported ${actualDocuments.length} Nova documents from ${sourceApplication}`)

function assertSafeTarget(target) {
  const expectedSuffix = path.join('docs', 'nova')
  if (!target.endsWith(expectedSuffix) || !target.startsWith(repositoryRoot)) {
    throw new Error(`Unsafe Nova docs target: ${target}`)
  }
}

function readDocsTree(source) {
  const timelineCategories = extractSourceBlock(
    source,
    'const TIMELINE_CHART_DOCS_CATEGORIES',
    'const TIMELINE_CHART_ROOT_OPTIONS',
  ).replace(': Array<DocsCategoryInput>', '')
  const docsTree = extractSourceBlock(
    source,
    'const DOCS_TREE',
    'export const DOCS_NAVIGATION_CATEGORIES',
  ).replace(': Array<DocsTreeInput>', '')

  const executableSource = `
    const category = (id, title, pages) => ({ id, title, pages })
    const page = (slug, title, caption, exampleId, contracts) => ({ slug, title, caption, exampleId, contracts })
    ${timelineCategories}
    ${docsTree}
    DOCS_TREE
  `

  return vm.runInNewContext(executableSource)
}

function extractSourceBlock(source, startMarker, endMarker) {
  const start = source.indexOf(startMarker)
  const end = source.indexOf(endMarker, start)
  if (start === -1 || end === -1) {
    throw new Error(`Cannot extract Nova navigation block: ${startMarker}`)
  }

  return source.slice(start, end)
}

async function copyPublicDocuments() {
  const documents = []
  const directories = (await readdir(sourceDocumentsRoot, { withFileTypes: true }))
    .filter(entry => entry.isDirectory())
    .sort((left, right) => left.name.localeCompare(right.name))

  for (const directory of directories) {
    const sourceDirectory = path.join(sourceDocumentsRoot, directory.name)
    const targetDirectory = path.join(targetDocumentsRoot, directory.name)
    await mkdir(targetDirectory, { recursive: true })

    const files = (await readdir(sourceDirectory, { withFileTypes: true }))
      .filter(entry => entry.isFile() && entry.name.endsWith('.md'))
      .sort((left, right) => left.name.localeCompare(right.name))

    for (const file of files) {
      const sourcePath = path.join(sourceDirectory, file.name)
      const targetPath = path.join(targetDirectory, file.name)
      const content = await readFile(sourcePath, 'utf8')
      await cp(sourcePath, targetPath)
      documents.push({
        documentationId: directory.name,
        slug: file.name.slice(0, -3),
        title: readDocumentTitle(content, file.name),
      })
    }
  }

  return documents
}

function readDocumentTitle(content, fallback) {
  return content.match(/^#\s+(.+)$/m)?.[1]?.trim() ?? fallback.replace(/\.md$/, '')
}

function createSidebar(tree, actualDocuments) {
  const registered = new Set()
  const sections = tree.map((documentation, documentationIndex) => {
    const categories = documentation.categories.map((category, categoryIndex) => ({
      text: category.title,
      collapsed: categoryIndex > 0,
      items: category.pages.flatMap(page => {
        const key = `${documentation.documentationId}/${page.slug}`
        if (registered.has(key)) return []
        registered.add(key)

        return [{
          text: page.title,
          link: `/nova/${documentation.documentationId}/${page.slug}`,
        }]
      }),
    }))

    const additionalDocuments = actualDocuments.filter(document => {
      const key = `${document.documentationId}/${document.slug}`
      return document.documentationId === documentation.documentationId && !registered.has(key)
    })

    if (additionalDocuments.length > 0) {
      categories.push({
        text: 'Дополнительные материалы',
        collapsed: true,
        items: additionalDocuments.map(document => ({
          text: document.title,
          link: `/nova/${document.documentationId}/${document.slug}`,
        })),
      })
      additionalDocuments.forEach(document => registered.add(`${document.documentationId}/${document.slug}`))
    }

    return {
      text: documentationTitles[documentation.documentationId] ?? documentation.documentationId,
      collapsed: documentationIndex > 0,
      items: categories,
    }
  })

  const orphanDocuments = actualDocuments.filter(document => {
    return !registered.has(`${document.documentationId}/${document.slug}`)
  })

  if (orphanDocuments.length > 0) {
    sections.push({
      text: 'Дополнительные материалы',
      collapsed: true,
      items: orphanDocuments.map(document => ({
        text: document.title,
        link: `/nova/${document.documentationId}/${document.slug}`,
      })),
    })
  }

  return sections
}
